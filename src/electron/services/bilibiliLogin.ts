/**
 * 获取 Bilibili 登录二维码
 */
export async function bilibiliGetQRCode(): Promise<BilibiliQRCodeResult> {
  const timestamp = new Date().toLocaleTimeString();

  try {
    console.log(`[${timestamp}] 请求 Bilibili 登录二维码...`);

    const response = await fetchJson<BilibiliQRGenerateResponse>(BILIBILI_QR_GENERATE_URL);

    if (response.code !== 0) {
      console.error(`[${timestamp}] Bilibili API 返回错误:`, response);
      return {
        success: false,
        error: response.message || '获取二维码失败',
      };
    }

    console.log(`[${timestamp}] 成功获取二维码, qrcode_key: ${response.data.qrcode_key}`);

    return {
      success: true,
      qrUrl: response.data.url,
      qrKey: response.data.qrcode_key,
    };
  } catch (error) {
    console.error(`[${timestamp}] 获取 Bilibili 二维码失败:`, error);
    return {
      success: false,
      error: error instanceof Error ? error.message : String(error),
    };
  }
}

/**
 * 检查 Bilibili 扫码状态
 */
export async function bilibiliCheckQRStatus(qrKey: string): Promise<BilibiliQRStatusResult> {
  const timestamp = new Date().toLocaleTimeString();

  try {
    const pollUrl = `${BILIBILI_QR_POLL_URL}?qrcode_key=${encodeURIComponent(qrKey)}`;

    // 使用 node-fetch 并获取 headers
    const fetch = (await import('node-fetch')).default;

    const response = await fetch(pollUrl, {
      headers: COMMON_HEADERS,
    });

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}: ${response.statusText}`);
    }

    const data = await response.json() as BilibiliQRPollResponse;

    if (data.code !== 0) {
      return {
        success: false,
        error: data.message || '检查状态失败',
      };
    }

    const statusCode = data.data.code;

    // 状态码含义:
    // 0 = 登录成功
    // 86038 = 二维码已过期
    // 86090 = 已扫码，等待确认
    // 86101 = 未扫码

    if (statusCode === 86101) {
      return {
        success: true,
        status: 'waiting',
      };
    }

    if (statusCode === 86090) {
      return {
        success: true,
        status: 'scanned',
      };
    }

    if (statusCode === 86038) {
      return {
        success: true,
        status: 'expired',
      };
    }

    if (statusCode === 0) {
      // 登录成功，从响应头获取 Cookie
      console.log(`[${timestamp}] Bilibili 登录成功!`);

      const setCookieHeaders = response.headers.raw()['set-cookie'] || [];
      const cookies = parseSetCookies(setCookieHeaders);

      // 从返回的 URL 中也可能包含一些信息，但主要 Cookie 在 Set-Cookie 头中

      if (cookies.size === 0) {
        // 某些情况下 Cookie 可能在 data.url 参数中
        // 尝试解析 URL 参数
        try {
          const url = new URL(data.data.url);
          const urlCookies = url.searchParams;

          // Bilibili 有时会在 URL 中返回这些参数
          const possibleCookies = ['DedeUserID', 'DedeUserID__ckMd5', 'SESSDATA', 'bili_jct'];
          for (const cookieName of possibleCookies) {
            const value = urlCookies.get(cookieName);
            if (value) {
              cookies.set(cookieName, {
                value,
                domain: '.bilibili.com',
                path: '/',
                secure: true,
                httpOnly: cookieName === 'SESSDATA',
              });
            }
          }
        } catch {
          // URL 解析失败，忽略
        }
      }

      if (cookies.size === 0) {
        console.warn(`[${timestamp}] 登录成功但未获取到 Cookie`);
        return {
          success: false,
          error: '登录成功但未获取到 Cookie，请重试',
        };
      }

      console.log(`[${timestamp}] 获取到 ${cookies.size} 个 Cookie`);

      // 保存 Cookie 文件
      const netscapeContent = toNetscapeFormat(cookies);
      const cookieDir = getCookieCacheDir();
      const cookieFile = path.join(cookieDir, 'bilibili.com.txt');

      fs.writeFileSync(cookieFile, netscapeContent, 'utf8');

      console.log(`[${timestamp}] Cookie 文件已保存: ${cookieFile}`);

      return {
        success: true,
        status: 'confirmed',
        cookieFile,
      };
    }

    // 未知状态
    console.warn(`[${timestamp}] 未知状态码: ${statusCode}`);
    return {
      success: false,
      error: `未知状态: ${data.data.message || statusCode}`,
    };
  } catch (error) {
    console.error(`[${timestamp}] 检查 Bilibili 扫码状态失败:`, error);
    return {
      success: false,
      error: error instanceof Error ? error.message : String(error),
    };
  }
}
