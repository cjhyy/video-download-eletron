// ============================================================================

/**
 * 发起 HTTP 请求
 */
async function fetchJson<T>(url: string, options?: RequestInit): Promise<T> {
  // 使用 Electron 的 net 模块或 node-fetch
  const fetch = (await import('node-fetch')).default;

  const response = await fetch(url, {
    ...options,
    headers: {
      ...COMMON_HEADERS,
      ...options?.headers,
    },
  });

  if (!response.ok) {
    throw new Error(`HTTP ${response.status}: ${response.statusText}`);
  }

  return response.json() as Promise<T>;
}

/**
 * 解析 Set-Cookie 头部，提取 Cookie
 */
function parseSetCookies(setCookieHeaders: string[]): Map<string, { value: string; domain: string; path: string; expires?: number; secure: boolean; httpOnly: boolean }> {
  const cookies = new Map<string, { value: string; domain: string; path: string; expires?: number; secure: boolean; httpOnly: boolean }>();

  for (const header of setCookieHeaders) {
    const parts = header.split(';').map(p => p.trim());
    const [nameValue, ...attributes] = parts;

    if (!nameValue) continue;

    const eqIndex = nameValue.indexOf('=');
    if (eqIndex === -1) continue;

    const name = nameValue.substring(0, eqIndex);
    const value = nameValue.substring(eqIndex + 1);

    let domain = '.bilibili.com';
    let cookiePath = '/';
    let expires: number | undefined;
    let secure = false;
    let httpOnly = false;

    for (const attr of attributes) {
      const [attrName, attrValue] = attr.split('=').map(s => s.trim());
      const lowerName = attrName.toLowerCase();

      if (lowerName === 'domain' && attrValue) {
        domain = attrValue.startsWith('.') ? attrValue : '.' + attrValue;
      } else if (lowerName === 'path' && attrValue) {
        cookiePath = attrValue;
      } else if (lowerName === 'expires' && attrValue) {
        expires = Math.floor(new Date(attrValue).getTime() / 1000);
      } else if (lowerName === 'max-age' && attrValue) {
        expires = Math.floor(Date.now() / 1000) + parseInt(attrValue);
      } else if (lowerName === 'secure') {
        secure = true;
      } else if (lowerName === 'httponly') {
        httpOnly = true;
      }
    }

    cookies.set(name, { value, domain, path: cookiePath, expires, secure, httpOnly });
  }

  return cookies;
}

/**
 * 转换为 Netscape Cookie 格式
 */
function toNetscapeFormat(cookies: Map<string, { value: string; domain: string; path: string; expires?: number; secure: boolean; httpOnly: boolean }>): string {
  let content = '# Netscape HTTP Cookie File\n';
  content += '# Generated by video-downloader-app (Bilibili QR Login)\n';
  content += '# https://curl.haxx.se/docs/http-cookies.html\n\n';

  for (const [name, cookie] of cookies) {
    const domain = cookie.domain;
    const flag = 'TRUE'; // domain 适用于所有子域
    const secure = cookie.secure ? 'TRUE' : 'FALSE';
    const expiry = cookie.expires || 0;

    content += `${domain}\t${flag}\t${cookie.path}\t${secure}\t${expiry}\t${name}\t${cookie.value}\n`;
  }

  return content;
}

